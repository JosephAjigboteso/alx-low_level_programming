#ifndef _MAIN_
#define _MAIN_

/**
 * _putchar - writes the character c to stdout
 *
 *  @c: The character to print
 *
 *  Return: On success 1.
 *
 * On error, -1 is returned, and errno is set appropriately.
 *
 */
int _putchar(char c);

/**
 * _strcat - append string src to string dest overwriting null character
 * @dest: first string to be overwritten
 * @src: second string
 * Return: pointer to string dest
 */
char *_strcat(char *dest, char *src);






#endif
